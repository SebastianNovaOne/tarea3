package logica;

/**
 * Clase que representa una moneda de 500 unidades.
 * Hereda de la clase Moneda.
 */
public class Moneda500 extends Moneda {

    /**
     * Retorna el valor de la moneda, que es 500.
     *
     * @return Valor de la moneda (500).
     */
    @Override
    public int getValor() {
        return 500;
    }
}
